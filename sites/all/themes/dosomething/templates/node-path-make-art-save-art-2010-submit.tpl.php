<h2><img alt="faq" src="/nd/hp_art/header_faq.png" /></h2>

<div id="question-answer">

<a>Why are DoSomething.org, HP, and AMD running &ldquo;Make Art. Save Art.&rdquo;?</a>
<p>DoSomething.org is committed to giving you ways to take action on causes you care about. HP and AMD believe that Art and Creativity are critical to a complete education. We want to give you a chance to show off your artistic talents while you take action to keep art education in schools!</p>

<a>What types of art are you looking for?</a>
<p>We strongly prefer digital artwork in one of the following formats: .ai, .eps, .psd, .jpg, .png, .tif, .gif. However, in the event that you are unable to submit your artwork in one of these formats, scanned images or digital photos of your artwork will also be accepted. Your work of art is meant to represent the importance of arts education and will be available for download as a PC wallpaper.</p>

<a>How do I win?</a>
<p>The 5 "most shared" designs will win! So, create a beautiful wallpaper design, write a compelling case on the importance of arts education, and encourage your friends, family, and social networks to share the heck out of your design. People will be able to share their favorite designs starting September 1, 2010.<br /><br />Sharing with state and local representatives is also really important. They're the ones who decide what schools need to include in their core curriculum. So, be sure to encourage sharing with both social networks and representatives!</p>

<a>Why does it matter if people share my design?</a>
<p>The key to advocating is getting your voice heard. Arts education is being cut from schools nationwide. If you want to Do Something about it, you need to speak up. Sharing online is a great way to spread the word and raise awareness about the importance of art education (not to mention, a great way to get more people to share your design and help you win).</p>

<a>How else can I take action to keep art in schools?</a>
<p>Encourage all of your friends and classmates to get involved in Make Art. Save Art. The more designs we get, the stronger our case that students want art in schools.</p>

<a>Itching to do more?</a>
<p>You can print and post your design around your school and community. Take pictures and send them to <a href="mailto:photos@dosomething.org">photos@dosomething.org</a></p>

